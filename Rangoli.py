import string

def print_rangoli(size):

    # Create a list of lowercase alphabet characters
    alphabet = string.ascii_lowercase

    # Create the upper half of the rangoli pattern
    linesss = []
    for i in range(size):
        line = '-'.join(alphabet[size-1:i:-1] + alphabet[i:size])
        linesss.append(line.center(4*size - 3, '-'))

    # Combine upper and lower half of the pattern
    rangoli_pattern = '\n'.join(linesss[::-1] + linesss[1:])

    print(rangoli_pattern)

if __name__ == '__main__':
    n = int(input())
    print_rangoli(n)
